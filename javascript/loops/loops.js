/* FOR LOOP */
let familyAtYourParty = 0
for (let i = 0; i < 10; i++) {
    familyAtYourParty++
}
console.log(familyAtYourParty)

for (let i = 10; i > 0; i--) {
    familyAtYourParty++
}

console.log(familyAtYourParty)

for (let i = 10; i > 0; i--) {
    familyAtYourParty--
}

