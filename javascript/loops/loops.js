/* WHILE LOOP */
let friendsAtYourParty = 0
while (friendsAtYourParty < 10) {
    console.log("another friend joines your party!")
    friendsAtYourParty = friendsAtYourParty + 1
}
console.log(friendsAtYourParty)

/* Shortcuts for adding one to a thing */
let siblingsAtYourParty = 0
siblingsAtYourParty = siblingsAtYourParty + 1
siblingsAtYourParty += 1
siblingsAtYourParty++
++siblingsAtYourParty
